    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        emptyisbn=false;
        count=0;
        name=jTextField1.getText();
        author=jTextField3.getText();
        pub=jTextField4.getText();
        sub=jTextField5.getText();
        try
        {
            isbn=Long.parseLong(jTextField2.getText());
        }
        catch(NumberFormatException e)
        {
            emptyisbn=true;
        }
        temp=isbn;
        while(temp>0)
        {
            temp/=10;
            count+=1;
        }
        if(emptyisbn||name.length()==0||author.length()==0||pub.length()==0||sub.length()==0)
        {
            showMessageDialog(this,"Please enter all/valid details");
        }
        else if(count!=13)
        {
            showMessageDialog(this,"Invalid ISBN");
        }
        else
        {       
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/library","root","");
            PreparedStatement stmt=con.prepareStatement("insert into books values(?,?,?,?,?);");
            stmt.setString(1,name);
            stmt.setString(2,author);
            stmt.setString(3,pub);
            stmt.setString(4,sub);
            stmt.setLong(5,isbn);
            stmt.executeUpdate();
            System.out.println("inserted");
            this.setVisible(false);
            new LibrarianSuccess().setVisible(true);
        }
        catch(Exception e){System.out.println(e);}        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

